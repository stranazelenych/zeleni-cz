<h2>SEBEVĚDOMÁ EVROPA</h2>

<p>Naše cesta na Ukrajinu a na její hranici s Ruskem byla úspěšná:
navázali jsme spolupráci s občanskými iniciativami i demokratickými
aktivisty. Zelení považují za přirozenou součást své politiky hledět
přes plot české zahrádky. Dnešní svět je neúprosně propojený a to co
se dnes děje nejen na Ukrajině, ale i na jiných místech světa, kde se
porušují lidská práva a hrozí válečné konflikty, se týká nás všech.
Takovou politiku, která si je těchto souvislostí vědoma, zelení hájí
všude, kde působí. A pevně věříme, že ji budeme moci prosazovat
i v Evropském parlamentu společně se zelenými z ostatních zemí Unie po
volbách, které nás už za několik týdnů čekají.</p>

