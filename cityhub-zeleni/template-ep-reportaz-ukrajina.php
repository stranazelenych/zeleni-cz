<h2>AKTIVIZOVAT MÍSTNÍ JEDINCE A INICIATIVY</h2>

<p>Navzdory tomu, jak je aktuální situace skličující, vznikají a působí
projekty, které se snaží rozvíjet občanskou společnost a posilovat
participativní demokracii. Proto je důležité podpořit aktivní jednotlivce
a iniciativy. Zvláště důležité je pomoci odstranit chronický nedostatek
informací masivní podporou nezávislých žurnalistů a iniciovat vznik
zpravodajských serverů, blogů apod. Nedostatečná informovanost je nejen
směrem z Ukrajiny do světa, ale i mezi jednotlivými ukrajinskými regiony.
Petr Machálek na základě cesty připravuje projektové záměry, které budou
podporovat právě rozvoj nezávislé žurnalistiky na Ukrajině, multiplikovat
úspěšné projekty do dalších regionů a vytvářet kanály, kterými si
budou organizace předávat informace.</p>

<p>Protože je občanská společnost v regionech slabá, plánuje Petr
Machálek také vytipovat nadějné regionální neziskové organizace, předat
jim zkušenosti nebo podporovat předávání úspěšného know-how ze zemí,
které jsou v podobné situaci.</p>

<h2>CESTA K DEMOKRACII BEZ KORUPCE</h2>

<p>Pozice Evropské unie i České republiky je dnes neudržitelně pasivní
tváří v tvář realitě. EU by měla užít nástrojů společné
zahraniční politiky EU a zahájit vlastní silnou pozorovatelskou misi na
východě Ukrajiny. Zatím je tam pouze OSN a OBSE, a to v malém počtu.</p>

<p>Evropská unie musí nabídnout Ukrajině pomoc při reformě státní
správy, decentralizaci země a posílení státních institucí – a to jak
ve formě know-how, tak v podobě vlastních expertů.</p>

<p>Je nezbytné navýšit prostředky pro rozvoj a podporu občanské
společnosti. Česká republika se musí především připojit k European
Endowment for Democracy. Ten by měl na východě vytvořit vlastní
informační centrum poskytující nezávislé informace pro místní, a také
finančně podporovat projekty cílící na zvýšení informovanosti
v regionu.</p>

<p>Ukrajina čelí dezinformační válce a stačí jediná provokace, která
může posloužit jako záminka pro invazi. Nutno zároveň říci, že Kyjev
přitom s východními regiony nevede dostatečný dialog. Ukrajinská státní
moc na východě prakticky neexistuje. Hrozí bezprecedentní konflikt
přinejmenším od dob Jugoslávie a nikdo dnes není připraven mu zabránit.
Česká republika i vlády členských států EU musí proto do konfliktu
skrze Evropskou unii okamžitě aktivně vstoupit a hledat mírovou cestu,
která zajistí zachování svrchovanosti Ukrajiny a zároveň ochranu práv
všech jejích obyvatel.</p>

