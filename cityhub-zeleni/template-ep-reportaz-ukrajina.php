    <h1>Reportáž Zelených z Ukrajiny</h1>
    <div class="lead">

<p>Na konci dubna jsem se jako zvláštní zmocněnec Evropské strany zelených
vydal osobně zjistit, jaká je aktuální situace na Ukrajině. Výstupem bude
návrh doporučení evropských Zelených pro další postup Evropské unie, a
také příprava projektů na podporu místní občanské společnosti.</p>

    </div>
  </div>
  </div></div>
  <div class="main">
<div class="part text"><div class="in">
  <div class="txt">

<p>K mému záměru se připojila také Heidi Hautala, bývalá finská
ministryně a spolupředsedkyně Zelené evropské nadace. Na cestu jsem
přizval také Petra Machálka, konzultanta řady českých nevládních
organizací a bývalého ředitele Hnutí DUHA, Nikolu Hořejše,
protikorupčního experta platformy Rekonstrukce státu, a dokumentaristu Jana
Látala, jehož úkolem bylo přinést z Ukrajiny svědectví
o aktuálním dění.</p>

<p>Občané Ukrajiny se dnes ocitají ve velmi obtížné situaci. Jejich země
je rozkradená, policie a státní zpráva zkorumpovaná, děti se stále ve
školách učí historii národa podle výkladu bývalé vlády a většina
obyvatel východu země si odtržení sice nepřeje, ale počítá s okupací
ruskou armádou jako s reálnou možností. Pro řadu z nás však není
těžké se do této situace vžít, protože jsme prošli v lecčems podobnou
zkušeností. I to je důvod, proč můžeme být obyvatelům Ukrajiny
nápomocni.</p>

<p>Společně s Petrem Machálkem jsme pro Vás připravili sumarizaci našich
hlavních postřehů z cesty, kdy jsme se setkali jak s místními politiky a
zástupci neziskových organizací, tak i vůdcem proruských separatistů.</p>

<h2>PSYCHOLOGICKÁ VÁLKA</h2>

<p>Na místě jsme se potkali s více než dvěma desítkami nezávislých
novinářů, politiků a občanských aktivistů. Náměstkyně ministra
školství Inna Sovsun nám popsala situaci v nové vládě: Míra rozkradení
Ukrajiny je tak obludná, že je jen stěží představitelná. Korupce je
všudypřítomná. Prostředky, které Evropa poskytla Ukrajině (11 mld EUR)
sotva stačí na to, aby země „zůstala v provozu“ a mohla zajišťovat
lidem základní potřeby. Reálnou moc v regionech mají pouze oligarchové.
Jen Achmatov zajišťuje v doněcké oblasti na 300 000 pracovních míst.
Ukrajina je navíc fatálně závislá na fosilních zdrojích a uranu z Ruska,
jejich podíl tvoří 93%.</p>

<p>Zástupci Helsinského výboru, který monitoruje stav lidských práv na
Ukrajině, nám potvrdili, že je neustále porušována svoboda projevu a
novináři i občanští aktivisté jsou zastrašováni. Přesto se lidé
nevzdávají a v Kyjevě řada iniciativ vzniká – příkladem je platforma
ukrajinských nevládních organizací a více než sta expertů, kteří
píší a prosazují tzv. reanimační balíček – nové protikorupční
zákony, za které lobují v médiích a především v parlamentu. Přímo na
Majdanu v hotelu vzniklo Ukrainian Crisis Media Center a v Kyjevě byla
založena také nezávislá internetová televize Hromadske.tv. Přestože
Ukrajina disponuje na první pohled mnoha sdělovacími prostředky, stav je
podle nezávislých novinářů velmi neutěšený. Média vlastní buď
oligarchové, kteří hrají vlastní hru s Putinem i vládou, nebo jsou jen
hlásnou troubou současné vlády jako v případě státní televize. Sami
Ukrajinci nejsou schopni verifikovat zprávy o dění v jejich zemi, natož
verifikovat, jak se o zemi referuje v západních médiích.</p>

