<h2>ZASTAVTE PROPAGANDU, TADY NENÍ FAŠISMUS</h2>

<p>Občanská společnost je na Ukrajině velice slabá, jak je typické pro
země postsovětského sektoru. Zatímco v Kyjevě počet iniciativ roste,
v regionech jsou to zatím jen jednotlivci, kterým schází podpora a pracují
za velmi skrovných podmínek. Všichni, s nimiž jsme se setkali, nás
utvrzovali v tom, že země není rozdělena jazykem či kulturou. Tato
dělící linie je pouze fikce živená zvláště ruskými médii. Desítky
lidí, se kterými jsme se setkali, opakovaně a podobnými slovy říkali:
„My všichni mluvíme ukrajinsky i rusky, naši zemi nerozděluje jazyk,
naši zemi rozdělují oligarchové a zájmy tlačící se zvenčí, které se
snaží zneužít situaci slabého státu.“ Podle sociologických průzkumů
80 % obyvatel na východě odmítá připojení k Rusku. Zároveň se však
většina z nich nijak neangažuje. Na východě tak vzniká občanské vakuum,
které propaganda Ruska, různých ukrajinských politických proudů
i mocných oligarchů dokáží zatím dobře využít.</p>

<p>V Doněcku okupují proruští separatisté komplex budov oblastní vlády.
Pronikali jsme k ní jen velmi pozvolna. Nejprve jsme se vyhýbali
i fotografování a natáčení v jejím okolí, nakonec jsme se dostali až
do samotného tábora separatistů. Ten dělí od okolního světa několik
barikád obehnaných ostnatým drátem. Rázem jsme se ocitli mezi zakuklenými
ozbrojenci, kteří na nádvoří mezi barikádami pálí ukrajinskou vlajku a
prohlašují, že jsou ve válce. Této zkušenosti předcházelo setkání
v jednom z Achmatovových hotelů s jedním z hlavních vůdců obsazeného
území Alexandrem Vaskovským. Ten uvedl, že se jim dostává přímé podpory
Ruska. Z kontextu bylo jasné, že mluví-li o podpoře, myslí tím zbraně,
finance i ozbrojené složky. Veřejně separatisté proklamují, že usilují
o referendum, v dalších slovech však otevřeně hovořil o tom, že
očekávají příchod ruských vojsk. Ani separatisté sami však nejsou
jednotní v tom, zda usilují o federalizaci, či rovnou o připojení
k Rusku. Mimo jiné separatisté disponují vlastními vojenskými specialisty,
zřejmě z Ruska, kteří mají perfektní přehled o minimální
akceschopnosti ukrajinské armády. Ta je z velké části neloajální,
v dezolátním personálním a technickém stavu. Pokud by tedy došlo ke vniku
ruských vojsk na východní Ukrajinu, bez vnější pomoci nemá šanci toto
území obhájit.</p>

