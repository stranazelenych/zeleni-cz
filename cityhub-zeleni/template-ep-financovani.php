  <h2>Seznam dárců</h2>
  <p>Seznam všech dárců, kteří podpořili finančně kampaň SZ do Evropského parlamentu, najdete na&nbsp;<a href="https://docs.google.com/spreadsheets/d/1NPOH0pmy3tyoZYLUknXuDLs05l6GXhgd9xZezwnMJo4/edit#gid=0" target="_blank">této adrese</a>.</p>
  <p>Dary zasílají dárci po vyplnění&nbsp;<a href="https://www.ondrejliska.cz/?daruj&amp;utm_source=financovani&amp;utm_medium=web&amp;utm_campaign=ceskodopredu#/" target="_blank">dárcovského formuláře</a>&nbsp;na&nbsp;<a href="https://www.fio.cz/scgi-bin/hermes/dz-transparent.cgi?ID_ucet=777555777" target="_blank">transparentní volební účet</a>.</p>
  <p>Dary přijaté platební kartou přijímáme na účet u&nbsp;<a href="http://www.rb.cz/firemni-finance/transparentni-ucty/?tr_acc=vypis&amp;account_number=2206946001" target="_blank">Raiffeisenbank</a>&nbsp;(označení úhrada za karty), odkud je pravidelně převádíme na transparentní volební účet.</p>
  <p>Na volební kampani Strany zelených se podílejí 327 dobrovolníků z celé České republiky. Každý den se přidávají další prostřednictvím webu <a href="http://kampanjstevy.cz/#/zapojse" target="_blank">www.kampanjstevy.cz</a>. Zpracování všech údajů probíhá striktně v souladu se zákonem na ochranu osobích údajů. Kromě toho nás podporuje řada odborníků a profesionálů z různých oborů. Nestydíme se za ně, a proto zde najdete jejich seznam.</p>
  <p>Protože si stojíme za tím, co děláme, najdete zde také kompletní přehled výdajů na kampaň, který průběžně doplňujeme. Uvádíme, kdo nám poskytuje slevy či služby zdarma, i subjekty, které nás podporují.</p>
