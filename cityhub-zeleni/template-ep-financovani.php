  <h3 class="txt-center">Financování volební kampaně do Evropského parlamentu</h3>

<p>Strana zelených schválila výdajový strop kampaně ve výši
3 997 000 Kč, ten bude navyšován v závislosti na příjmové stránce
(výsledcích fundraisingu). Vyrovnaný rozpočet volební kampaně nepřesáhne
částku 6 342 000 Kč. Na straně příjmů se počítá s těmito
příjmy:</p>

  <ul>
    <li>příjmem ze stranického rozpočtu ve výši 1,5 mil Kč</li>
    <li>0,1 mil Kč dar hnutí Změna</li>
    <li>individuální fundraising až 2,9 mil Kč</li>
    <li>podpora individálního fundraisingu Karlem Janečkem až 1,8 mil Kč (<a href="https://www.ondrejliska.cz/files/Janecek-Karel-darovaci-smlouva.pdf" target="_blank">darovací smlouva</a>)</li>
  </ul>
