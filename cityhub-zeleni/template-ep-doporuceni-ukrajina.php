    <h1>12 doporučení pro řešení situace na Ukrajině</h1>
    <div class="lead">

<p>Předseda zelených Ondřej Liška na základě své nedávné cesty do
Kyjeva a Doněcku jako zvláštní vyslanec Evropské strany zelených inicioval
formulaci dvanácti doporučení pro řešení situace na Ukrajině.</p>

    </div>
  </div>
  </div></div>
  <div class="main">
<div class="part text"><div class="in">
  <div class="txt">

<p>Spolu s přední finskou zelenou političkou Heidi Hautala, která se
k jeho misi připojila, je nyní předkládá k veřejné diskusi a spolu
s evropskými zelenými bude po volbách v Evropském parlamentu prosazovat
jejich naplnění. Vývoj na Ukrajině se mění každým dnem a tato
doporučení mohou rychle zastarat. Proto sledujeme situaci nadále a budeme
pokračovat v hledání aktivního řešení skrze možnosti a nástroje,
které má v rukou Evropská unie a Česká republika jako její člen.</p>

<ol>
  <li>Evropská unie (EU) musí podpořit Organizaci pro bezpečnost a spolupráci
  v Evropě (OBSE)<sup>[1]</sup>, která je hlavním politickém fórem
  k jednání o nalezení mírového řešení situace na Ukrajině.
  Především by měla usilovat o rozšíření působnosti současné
  Zvláštní monitorovací mise OBSE pro Ukrajinu<sup>[2]</sup> a významné
  navýšení jejího personálu.</li>

  <li>EU by měla zajistit pro OBSE dostatečný počet pozorovatelů
  dohlížejících na prezidentské volby, které se na Ukrajině konají
  25. května. K tomu by EU měla plně využít Nástroje EU pro podporu
  stability a míru (IcSP)<sup>[3]</sup>. Je však rovněž nutné další
  jednání v oblasti zprostředkování pokojného řešení sporů. EU by mohla
  podpořit misi OBSE obrněnými vozidly a dalším vybavením nezbytným pro
  její bezpečnou práci.</li>

  <li>Jakmile bude nalezena politická shoda na ukončení současného
  násilného konfliktu, měla by EU zvážit vytvoření další civilní mise
  vedle mise OBSE, se zaměřením na reformu bezpečnostních složek a
  budování právního státu.</li>

  <li>EU by měla pokračovat ve svém úsilí o propuštění desítek zajatců,
  kteří jsou stále drženi ve vazbě a mohou být vystaveni nelidskému
  zacházení či mučení.</li>

  <li>EU by měla využít nástroje European Endowment for Democracy
  (EED)<sup>[4]</sup> na podporu občanské společnosti na Ukrajině, zejména
  k posílení místních lidskoprávních nevládních organizací a organizací
  prosazujících práva žen. Členské státy EU by měly navýšit svou
  finanční podporu tohoto nástroje a země, které podobně jako ČR dosud
  nejsou jeho součástí, by se měly okamžitě zapojit. EU by měla podporovat
  nezávislé a občanské mediální projekty. Obdobně by měly být podpořeny
  nevládní iniciativy informující o případech porušováí lidských práv
  a pomáhající obětem těchto porušení.</li>

  <li>Ukrajina by měla souhlasit s nezávislým mezinárodním vyšetřováním
  případů porušení lidských práv. Evropská unie musí přispět ke
  zlepšení financování a posílení personálu Monitorovací mise OSN pro
  lidská práva na Ukrajině<sup>[5]</sup>, zejména na východě země.
  Evropská unie by se měla dále zasadit o předložení zprávy o situaci na
  Ukrajině Radě pro lidská práva OSN v Ženevě<sup>[6]</sup>.</li>

  <li>EU by měla ukrajinskou vládu podpořit při reformě veřejné správy a
  posílení veřejných institucí prostřednictvím výuky, práce expertů,
  asistovaných projektů atd. Tento balíček by zahrnoval pomoc při zaručení
  práv menšin, decentralizaci státní správy a zajištění účasti
  veřejnosti. K tomu je třeba využít expertízy Vysokého komisaře OBSE pro
  národnostní menšiny, Rady Evropy a Organizace pro hospodářskou spolupráci
  a rozvoj (OECD)<sup>[7]</sup>, stejně jako expertízy orgánů OSN.</li>

  <li>EU by měla zvážit předložení nabídky jednání prezidentu Putinovi
  s nejvyššími představiteli států EU. To by mohlo vytvořit prostor pro
  diplomatické řešení a k přesvědčení Ruska, že budoucnost Evropy je
  podmíněna svrchovanou, demokratickou a stabilní Ukrajinou. Je potřeba
  zajistit, aby mohly 25. května hladce proběhnout ukrajinské prezidentské
  volby a co nejdříve poté volby parlamentní.</li>

  <li>Členské státy EU by měly využít všech možností k rozšíření
  jejich programů studentské výměny pro celé území Ukrajiny. Studenti
  z Ukrajiny by měli mít možnost získat dlouhodobá víza. EU by měla
  dlouhodobá víza udělovat také ukrajinským občanským aktivistům a
  obráncům lidských práv.</li>

  <li>EU by měla zastavit veškerý vývoz zbraní a obdobného materiálu do
  Ruska.. Zbraně by se neměly vyvážet ani ukrajinským bezpečnostním
  složkám.</li>

  <li>Evropská unie potřebuje jednotnou politiku vůči Rusku. Je třeba
  pokračovat s využití diplomatických nástrojů a hospodářských sankcí.
  EU nemůže Rusku dovolit ohrožovat mír na svém kontinentě
  rozdmýcháváním nacionalismu. Pokud Rusko nebude spolupracovat na pokojném
  řešení konfliktu, měla by EU přijmout další a rozsáhlejší sankce.</li>

  <li>Je třeba diskutovat o nahrazení dovozu plynu a ropy z Ruska, včetně
  pomoci členským státům EU, které jsou na dovozu ruských energií stále do
  velké míry závislé. Evropská komise by měla zahrnout Ukrajinu do plánu
  energetické nezávislosti EU, který má být představen v červnu
  tohoto roku.</li>
</ol>

